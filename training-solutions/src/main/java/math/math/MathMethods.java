package math.math;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(12,23));
        System.out.println(Math.min(23,12));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(23.43));
        System.out.println(Math.abs(-54));
        System.out.println(Math.negateExact(5));
        System.out.println(Math.addExact(4,6));
        System.out.println(Math.subtractExact(3,5));
        System.out.println(Math.multiplyExact(4,5));
        System.out.println(Math.pow(2,3));
        System.out.println(Math.incrementExact(3));
        System.out.println(Math.decrementExact(3));
        System.out.println(Math.random());
    }
}
